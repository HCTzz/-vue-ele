import clsIDCard from '@/utils/clsIDCard'
import { backstageValid as commonValid } from '@/api/validate'
/**
 * @param {string} path
 * @returns {Boolean}
 */
export function isExternal(path) {
    return /^(https?:|mailto:|tel:)/.test(path)
}

